export function t(key: string, params?: Record<string, string | number>): string {
  const tmpl = T[key]?.[settingsStore.locale] ?? key;
  if (!params) return tmpl;
  return tmpl.replace(/\{(\w+)\}/g, (_, k: string) => String(params[k] ?? ''));
}

const timeGroupKeyMap: Record<TimeGroup, string> = {
  'Today': 'time.today',
  'Yesterday': 'time.yesterday',
  'This week': 'time.thisWeek',
  'This month': 'time.thisMonth',
  'Older': 'time.older',
};

export function timeGroupLabel(g: TimeGroup): string {
  return t(timeGroupKeyMap[g]);
}
